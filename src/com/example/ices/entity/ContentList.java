package com.example.ices.entity;

public class ContentList {
	private int id;
	private String contentTitle;
	private String contentShortText;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getContentTitle() {
		return contentTitle;
	}
	public void setContentTitle(String contentTitle) {
		this.contentTitle = contentTitle;
	}
	public String getContentShortText() {
		return contentShortText;
	}
	public void setContentShortText(String contentShortText) {
		this.contentShortText = contentShortText;
	}
	
}
